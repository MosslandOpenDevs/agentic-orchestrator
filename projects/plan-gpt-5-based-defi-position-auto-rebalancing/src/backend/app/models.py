from sqlalchemy import Column, Integer, String, Float, BigInteger, DateTime, UUID
from sqlalchemy.ext.declarative import declarative_base
from sqlalchemy.orm import back_populates, relationship

Base = declarative_base()

class User(Base):
    """Represents a user of the Mossland Lending Protocol."""

    __tablename__ = 'users'

    id = Column(UUID, primary_key=True)
    userId = Column(String, unique=True)
    riskTolerance = Column(Float)
    investmentStrategy = Column(String)
    createdAt = Column(DateTime)
    updatedAt = Column(DateTime)

    def __repr__(self):
        return f"<User(userId='{self.userId}', riskTolerance={self.riskTolerance})>"

    portfolio_positions = relationship("PortfolioPosition", back_populates="user")


class SecurityToken(Base):
    """Represents a tokenized security asset within the Mossland ecosystem."""

    __tablename__ = 'security_tokens'

    id = Column(UUID, primary_key=True)
    tokenId = Column(String, unique=True)
    name = Column(String)
    symbol = Column(String)
    decimals = Column(Integer)
    totalSupply = Column(BigInteger)
    price = Column(Float)
    createdAt = Column(DateTime)
    updatedAt = Column(DateTime)

    def __repr__(self):
        return f"<SecurityToken(tokenId='{self.tokenId}', name='{self.name}', symbol='{self.symbol}')>"

    portfolio_positions = relationship("PortfolioPosition", back_populates="security_token")


class PortfolioPosition(Base):
    """Represents a holding of a SecurityToken within a portfolio."""

    __tablename__ = 'portfolio_positions'

    id = Column(UUID, primary_key=True)
    positionId = Column(String, unique=True)
    userId = Column(String, index=True)
    tokenId = Column(String, index=True)
    quantity = Column(BigInteger)
    entryPrice = Column(Float)
    lastUpdated = Column(DateTime)
    createdAt = Column(DateTime)
    updatedAt = Column(DateTime)

    def __repr__(self):
        return f"<PortfolioPosition(positionId='{self.positionId}', userId='{self.userId}', tokenId='{self.tokenId}')>"

    user = back_populates("user", viewfunc=None)
    security_token = back_populates("security_token", viewfunc=None)


class RebalancingRecommendation(Base):
    """Represents a recommendation generated by SynapseAI for adjusting portfolio positions."""

    __tablename__ = 'rebalancing_recommendations'

    id = Column(UUID, primary_key=True)
    recommendationId = Column(String, unique=True)
    userId = Column(String, index=True)
    tokenId = Column(String, index=True)
    changeQuantity = Column(BigInteger)
    reason = Column(String)
    createdAt = Column(DateTime)
    updatedAt = Column(DateTime)

    def __repr__(self):
        return f"<RebalancingRecommendation(recommendationId='{self.recommendationId}', userId='{self.userId}', tokenId='{self.tokenId}')>"

    user = back_populates("user", viewfunc=None)
    security_token = back_populates("security_token", viewfunc=None)


if __name__ == '__main__':
    from sqlalchemy import create_engine
    engine = create_engine('sqlite:///:memory:')
    Base.metadata.create_all(bind=engine)